export interface User {
  _id: string; //id auto generated by mongo
  email: string;
  role: 'admin' | 'responsable_region' | 'user'; // enum
  isVerified: boolean;
  isActive: boolean;
  createdAt?: string; // timestamps
  updatedAt?: string;
}

// login response
export interface LoginResponse {
  user: User;
  token: string;
}
